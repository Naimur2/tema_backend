    /**
     * Store a newly created resource in storage.
     *
     * @param  \App\Http\Requests\StoreFileRequest  $request
     * @return \Illuminate\Http\Response
     */
    public function store(StoreFileRequest $request)
    {
        $path = 'files';
        if (!Storage::disk('public')->path($path)) {
            Storage::disk('public')->makeDirectory($path);
        }

        $fileName = $request->file('attachment')->getClientOriginalName();

        $path = Storage::disk('public')->putFile($path, $request->file('attachment'));

        File::query()
            ->create(array_merge($request->validated(), ['name' => $fileName, 'file' => $path]));

        return redirect()->back()->withSuccess('Successfully new file created');
    }

